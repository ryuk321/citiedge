export interface StudentInfo {
  id?: number; // optional, auto-generated by DB
  student_number: string;
  first_name: string;
  last_name: string;
  email: string;
  phone?: string;
  dob?: string; // YYYY-MM-DD
  age?: number;
  gender?: "Male" | "Female" | "Other";
  address?: string;
  nationality?: string;
  course_id: number;
  enrollment_date?: string; // YYYY-MM-DD
  intake?: string; // e.g. "Fall 2025"
  status?: "active" | "inactive" | "graduated" | "withdrawn";
  created_at?: string;
  updated_at?: string;
}

export interface User {
  id: number;                     // unique identifier
  email: string;                   // login email
  password_hash: string;           // hashed password (never store plain text!)
  username?: string;                   // optional display name
  role: "student" | "admin" | "staff"; // roles for access control
  created_at: string;              // timestamp (ISO string from DB)
  updated_at: string;    
  status:string;          // timestamp (ISO string from DB)
}